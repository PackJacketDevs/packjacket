/**
 * An action to open up the PackJacket website
 * @author Amandeep Grewal
 */
class ClickAction extends AbstractAction {

    /**
     * Creates the action
     */
    public ClickAction() {
        super.putValue(Action.NAME, "http://packjacket.sf.net");
        super.putValue(Action.SHORT_DESCRIPTION, "Packjacket Website");
    }

    /**
     * Opens the website using Desktop class
     * @param e info about click
     */
    @Override
    public void actionPerformed(ActionEvent e) {
        try {
            Desktop.getDesktop().browse(new URI("http://packjacket.sf.net"));
        } catch (Exception e2) {
            RunnerClass.logger.log(Level.SEVERE, null, e2);
        }
    }
}
